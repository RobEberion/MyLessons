    internal class Modul_02_Variablen_und_Datentypen
    {
        static void Main(string[] args)
        {
            //ANFANG--------------------------------------------------------------------------------
            //--------------------------------------------------------------------------------------

            //Variablen deklarieren und initialiesieren---------------------------------------------
            //--------------------------------------------------------------------------------------

            //Deklaration
            string username;

            //Initialisierung
            username = "Robert";

            Console.WriteLine(username);

            username = "Ralf";
            int age = 10;

            Console.WriteLine($"Alter: {age}");
            Console.WriteLine($"Name des Benutzers: {username}");

            Console.ReadKey();
            Console.WriteLine();

            //--------------------------------------------------------------------------------------
            //Datentypen----------------------------------------------------------------------------

            /*Ganzzahlige Datentypen----------------------------------------------------------------
            *byte                                       0 - 255                              8 Bit
            *short                                -32.768 - 32.767                          16 Bit
            *int                           -2.147.483.648 - 2.147.483.647                   32 Bit
            *long              -9.223.372.036.854.775.808 - 9.223.372.036.854.775.807       64 Bit
            *sbyte                                   -128 - 127                              8 Bit
            *ushort                                     0 - 65.535                          16 Bit
            *uint                                       0 - 4.294.967.295                   32 Bit
            *ulong                                      0 - 18.446.744.073.709.551.615      64 Bit
            --------------------------------------------------------------------------------------*/

            sbyte zahl1 = -100;
            ushort zahl2 = 20000;
            uint zahl3 = 2000000;
            ulong zahl4 = 200000000;

            Console.WriteLine(zahl1);

            Console.ReadKey();
            Console.WriteLine();

            /*Fließkommazahlen Datentypen-----------------------------------------------------------
            *float                          -3,402823E+38 - 3,402823E+38         
            *                                          ~6 - 9 Stellen                        4 Bytes
            *double                -1,79769313486232E+308 - 1,79769313486232E+308
            *                                         ~15 - 17 Stellen                       8 Bytes
            *decimal       -79228162514264337593543950335 - 79228162514264337593543950335    
            *                                          28 - 29 Stellen                      16 Bytes
            --------------------------------------------------------------------------------------*/

            float zahl5 = 2.25F;
            double zahl6 = 33.3245;
            decimal zahl7 = 32.45M;

            Console.WriteLine(zahl6);

            Console.ReadKey();
            Console.WriteLine();

            //Textdatentypen String und Char--------------------------------------------------------

            string text = "Hallo Welt!";
            char character = 'B';
            char character2 = text[0];

            Console.WriteLine(text);            //String
            Console.WriteLine(character);       //Char
            Console.WriteLine(character2);      //Char B
            Console.WriteLine(text[0]);         //Char im String

            Console.ReadKey();
            Console.WriteLine();

            //Booleans------------------------------------------------------------------------------

            bool wahrheitswert = true;
            bool wahrheitswert2 = false;

            Console.WriteLine(wahrheitswert);
            Console.WriteLine(wahrheitswert2);

            Console.ReadKey();
            Console.WriteLine();

            //Eingaben vom Benutzer entgegennehmen--------------------------------------------------
            //--------------------------------------------------------------------------------------

            //Eingabe mit String
            Console.WriteLine("Gebe deinen Namen ein: ");
            string name = Console.ReadLine();

            //Eingabe mit Zahlen, muss konvertiert werden
            Console.WriteLine("Gebe dein Alter ein: ");
            int age2 = Convert.ToInt32(Console.ReadLine());

            Console.WriteLine($"Hallo {name} du bist {age2} Jahre alt");

            Console.ReadKey();

            //ENDE----------------------------------------------------------------------------------
            //--------------------------------------------------------------------------------------
        }
    }

